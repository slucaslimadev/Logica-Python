#**Problema 10:** Calcule o sucessor e o antecessor de um número.
print("--- **Problema 10:** Calcule o sucessor e o antecessor de um número. ---")
numero = int(input("Digite um número para calcular o antecessor e sucessor: "))
sucessor = numero + 1
antecessor = numero - 1
print(f"Antecessor: {antecessor}\nNúmero: {numero}\nSucessor: {sucessor}")